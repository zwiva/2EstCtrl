#1.6 Utiliza álgebra booleana para hacer un refactoring de este código. Para verificar la evaluación de condiciones puedes modificar los valores de a y b.

a = 'verdadero'
b = 'falso'

if a == 'verdadero' && b == 'verdadero'
    puts ':)'
elsif a == 'falso' && b == 'verdadero'
    puts ':|'
else 
    puts ':('
    
end